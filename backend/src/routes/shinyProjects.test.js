const SOURCE_BYTES = Buffer.from('image bytes').toString('base64');
const GENERATED_BYTES = Buffer.from('generated bytes').toString('base64');

function memoryStore() {
  let t = 0;
  const objects = new Map();
  return {
    configured: true,
    pushes: [],
    async push(payload) {
      this.pushes.push(payload);
      const intent = payload.request.intents[0];
      const key = `${intent.ownerKind}:${intent.ownerId}:${intent.collection}:${intent.objectId}`;
      const existing = objects.get(key);
      if (intent.action === 'updateObject' && existing && intent.baseObjectT !== existing.changedT) {
        return {
          replicaEpoch: payload.request.replicaEpoch,
          baseT: payload.request.baseT,
          toT: t,
          results: [{
            intentId: intent.id,
            status: 'conflict',
            t: existing.changedT,
            objectId: intent.objectId,
            reason: 'staleBaseObjectT',
          }],
          objects: [existing],
        };
      }
      t += 1;
      const object = {
        id: intent.objectId,
        ownerKind: intent.ownerKind,
        ownerId: intent.ownerId,
        collection: intent.collection,
        createdT: existing?.createdT || t,
        changedT: t,
        deletedT: null,
        createdAt: existing?.createdAt || intent.createdAt,
        changedAt: intent.createdAt,
        deletedAt: null,
        codec: 'json',
        encryptionMode: 'none',
        payloadJson: intent.payloadJson,
        payloadBytes: null,
        payloadHash: intent.payloadHash,
        schemaVersion: 1,
      };
      objects.set(key, object);
      return {
        replicaEpoch: payload.request.replicaEpoch,
        baseT: payload.request.baseT,
        toT: t,
        results: [{
          intentId: intent.id,
          status: 'accepted',
          t,
          objectId: intent.objectId,
          reason: null,
        }],
        objects: [object],
      };
    },
    async pull({ actorUserId }) {
      return {
        replicaEpoch: '01973e36-4c80-7abc-8a72-222222222222',
        fromT: 0,
        toT: t,
        hasMore: false,
        objects: Array.from(objects.values()).filter(object => object.ownerId === actorUserId),
      };
    },
    async listObjects({ collection }) {
      return Array.from(objects.values()).filter(object => object.collection === collection);
    },
  };
}

async function buildApp(store, deps = {}) {
  const app = Fastify({ logger: false, bodyLimit: 25 * 1024 * 1024 });
  await registerShinyProjectRoutes(app, { localReplicaStore: store, ...deps });
  await app.ready();
  return app;
}

async function uploadProject(app) {
  return app.inject({
    method: 'POST',
    url: '/api/shiny/projects',
    headers: {
      'content-type': 'application/json',
      'x-jobdone-device-id': 'device-1',
    },
    body: JSON.stringify({
      projectId: PROJECT_ID,
      ownerUserId: OWNER_ID,
      filename: 'dog.jpg',
      mimeType: 'image/jpeg',
      dataBase64: SOURCE_BYTES,
    }),
  });
}

